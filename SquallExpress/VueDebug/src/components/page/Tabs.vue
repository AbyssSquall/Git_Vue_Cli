<script>
    export default {
        name: 'tabs',
        data() {
            return {
                message: 'first',
                showHeader: false,
                unread: [{
                    date: '2018-04-19 20:00:00',
                    title: '【系统通知】该系统将于今晚凌晨2点到5点进行升级维护',
                },{
                    date: '2018-04-19 21:00:00',
                    title: '今晚12点整发大红包，先到先得',
                }],
                read: [{
                    date: '2018-04-19 20:00:00',
                    title: '【系统通知】该系统将于今晚凌晨2点到5点进行升级维护'
                }],
                recycle: [{
                    date: '2018-04-19 20:00:00',
                    title: '【系统通知】该系统将于今晚凌晨2点到5点进行升级维护'
                }]
            }
        },
        methods: {
            handleRead(index) {
                const item = this.unread.splice(index, 1);
                console.log(item);
                this.read = item.concat(this.read);
            },
            handleDel(index) {
                const item = this.read.splice(index, 1);
                this.recycle = item.concat(this.recycle);
            },
            handleRestore(index) {
                const item = this.recycle.splice(index, 1);
                this.read = item.concat(this.read);
            }
        },
        mounted() {
            var that = this;
            this.$axios({
                method: 'post',
                url: '/nbmz',
                headers:{
                    //"Access-Control-Allow-Origin":"*",
                    Accept: "text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng,*/*;q=0.8",
                },
                data: this.$qs.stringify({
                    Name:"111111",
                    Date:"111111"
                })
            }).then((_Res)=>{
                var FirstFlag = that.QXList.length==0;
                //that.QXList = [];
                var TempData = JSON.parse(this.xml.parse(_Res.data).string);

                for(var QXIndex in TempData){
                    //添加区县列表
                    if(FirstFlag)
                        that.QXList.push({qx_name:TempData[QXIndex].name});

                    //添加全部数据
                    if(TempData[QXIndex].time)
                        TempData[QXIndex].time = that.MS_Date + TempData[QXIndex].time;
                    else
                        TempData[QXIndex].time = that.MS_Date;

                    that.TotalTableData.push(TempData[QXIndex]);
                }

                that.TotalPageCount = that.TotalTableData.length/12;
                that.tableData = that.TotalTableData.splice((that.cur_page-1)*12,12);
            }).catch((_Err)=>{
                console.log(_Err)
            })
        },
        computed: {
            unreadNum(){
                return this.unread.length;
            }
        }
    }

</script>

